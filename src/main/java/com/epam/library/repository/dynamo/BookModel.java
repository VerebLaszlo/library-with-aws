package com.epam.library.repository.dynamo;

import com.epam.library.model.*;
import com.epam.library.util.*;

import com.amazonaws.services.dynamodbv2.datamodeling.*;
import org.jetbrains.annotations.*;

import java.util.*;

@DynamoDBTable(tableName = BookModel.TABLE_NAME)
public class BookModel {
    public static final String TABLE_NAME = "Books";
    public static final String HASH_KEY_NAME = "id";

    @DynamoDBHashKey(attributeName = HASH_KEY_NAME)
    @DynamoDBAutoGeneratedKey
    private @Nullable String id;
    private String title;
    private String author;
    private String publisher;
    private String isbn;

    public BookModel() {
        this(null, "", "", "", "");
    }

    public BookModel(@Nullable String id, String title, String author, String publisher, Isbn isbn) {
        this.id = id;
        this.title = title;
        this.author = author;
        this.publisher = publisher;
        this.isbn = isbn.getNumber();
    }

    public BookModel(@Nullable String id, String title, String author, String publisher, String isbn) {
        this.id = id;
        this.title = title;
        this.author = author;
        this.publisher = publisher;
        this.isbn = isbn;
    }

    public @Nullable String getId() {
        return id;
    }

    public void setId(@Nullable String id) {
        this.id = id;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getAuthor() {
        return author;
    }

    public void setAuthor(String author) {
        this.author = author;
    }

    public String getPublisher() {
        return publisher;
    }

    public void setPublisher(String publisher) {
        this.publisher = publisher;
    }

    public String getIsbn() {
        return isbn;
    }

    public void setIsbn(String isbn) {
        this.isbn = isbn;
    }

    @SuppressWarnings("NonFinalFieldReferencedInHashCode")
    @Justification("Setters are required, finals can not be used")
    @Override
    public int hashCode() {
        return Objects.hash(title, author, publisher, isbn);
    }

    @SuppressWarnings("NonFinalFieldReferenceInEquals")
    @Justification("Setters are required, finals can not be used")
    @Override
    public boolean equals(Object obj) {
        if (this == obj) return true;
        if (obj == null || getClass() != obj.getClass()) return false;
        BookModel book = (BookModel) obj;
        return title.equals(book.title) &&
               author.equals(book.author) &&
               publisher.equals(book.publisher) &&
               isbn.equals(book.isbn);
    }

    @Override
    public String toString() {
        return String.format("BookModel{id='%s', title='%s', author='%s', publisher='%s', isbn='%s'}",
                             id, title, author, publisher, isbn);
    }
}
